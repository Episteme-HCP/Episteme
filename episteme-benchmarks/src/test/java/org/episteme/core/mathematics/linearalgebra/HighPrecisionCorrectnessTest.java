package org.episteme.core.mathematics.linearalgebra;

import org.episteme.core.mathematics.numbers.real.RealBig;
import org.episteme.core.mathematics.numbers.complex.Complex;
import org.episteme.benchmarks.benchmark.BenchmarkResult;
import org.episteme.benchmarks.reporting.BenchmarkReporter;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.BeforeAll;
import org.junit.jupiter.api.AfterAll;
import org.junit.jupiter.api.Assertions;

import java.util.*;

/**
 * Correctness audit for High-Precision Linear Algebra operations.
 * Verifies numerical accuracy and ensures no fallback to double occurs for 68+ operations.
 */
public class HighPrecisionCorrectnessTest {

    private static org.episteme.benchmarks.util.ServerOrchestrator orchestrator;

    @BeforeAll
    public static void startServer() {
        if (Boolean.getBoolean("episteme.test.skip-server-startup")) {
            System.out.println("Skipping Episteme Server startup (episteme.test.skip-server-startup=true)");
            return;
        }

        // Force high precision for the entire audit session
        org.episteme.core.mathematics.context.MathContext.setCurrent(
            org.episteme.core.mathematics.context.MathContext.withPrecision(100));
        
        org.episteme.core.technical.algorithm.AlgorithmManager.setService(new org.episteme.core.technical.algorithm.StandardAlgorithmService());
        try {
            orchestrator = new org.episteme.benchmarks.util.ServerOrchestrator();
            orchestrator.startServer();
            System.out.println("Episteme Server started successfully for correctness audit (100-digit context enabled).");
        } catch (Exception e) {
            System.err.println("Failed to start Episteme Server via Orchestrator (it might be already running): " + e.getMessage());
            e.printStackTrace();
        }
    }

    @AfterAll
    public static void stopServer() {
        if (orchestrator != null) {
            orchestrator.stopAll();
        }
    }

    private static final int MATRIX_SIZE = 3;
    
    private static final Set<String> ALLOWED_PROVIDERS = Set.of(
        "Episteme (Standard)",
        "Episteme (CARMA)",
        "Episteme CPU (Dense)",
        "Episteme CPU (Sparse)",
        "Episteme (Strassen)",
        "Distributed Linear Algebra Provider (LocalDistributedContext)",
        "Native MPFR Sparse Linear Algebra Backend",
        "Native MPFR Dense Linear Algebra Backend"
    );

    @Test
    public void runAudit() {
        BenchmarkReporter reporter = new BenchmarkReporter("High-Precision Correctness Audit");
        reporter.setComments("Verifying numerical correctness and fallback prevention for 68+ operations. Tolerance: 1E-25 (Real), 1.0E-8 (Complex).");
        reporter.setFooter("*Generated by HighPrecisionCorrectnessTest on " + new java.util.Date() + "*");
        reporter.addMetadata("Precision", org.episteme.core.mathematics.context.MathContext.getCurrent().getJavaMathContext().getPrecision() + " decimal digits (" + (int)(org.episteme.core.mathematics.context.MathContext.getCurrent().getJavaMathContext().getPrecision() * 3.322) + " bits)");
        reporter.addMetadata("JDK", System.getProperty("java.version") + " (" + System.getProperty("java.vendor") + ")");
        reporter.addMetadata("OS", System.getProperty("os.name") + " " + System.getProperty("os.arch"));

        List<LinearAlgebraProvider<?>> discovered = discoverHPProviders();
        System.out.println("Discovered Providers: " + discovered.size());
        for (LinearAlgebraProvider<?> p : discovered) System.out.println(" - " + p.getName());

        try {
            for (LinearAlgebraProvider<?> p : discovered) {
                String name = p.getName();
                if (!ALLOWED_PROVIDERS.contains(name)) {
                    System.out.println("Skipping Provider (not in allowlist): " + name);
                    continue;
                }
                
                System.out.println("Auditing Provider: " + name);
                Map<String, String> metrics = new LinkedHashMap<>();
                
                // Ground truth uses Native MPFR (highest available local precision)
                try (LinearAlgebraProvider<RealBig> groundTruth = new org.episteme.nativ.mathematics.linearalgebra.backends.NativeMPFRDenseLinearAlgebraBackend<>()) {
                    auditProvider(p, groundTruth, metrics);
                } catch (Exception e) {
                    System.err.println("Error closing ground truth provider: " + e.getMessage());
                }
                
                BenchmarkResult result = new BenchmarkResult(name, "Linear Algebra (High-Precision Correctness)", metrics);
                reporter.addResult(result);
            }
        } finally {
            for (LinearAlgebraProvider<?> p : discovered) {
                try {
                    p.close();
                } catch (Exception e) {
                    System.err.println("Warning: Failed to close provider " + p.getName() + ": " + e.getMessage());
                }
            }
        }

        boolean hasFailures = false;
        for (BenchmarkResult r : reporter.getResults()) {
            for (String key : r.extraMetrics().keySet()) {
                if (String.valueOf(r.extraMetrics().get(key)).contains("FAIL")) {
                    hasFailures = true;
                }
            }
        }

        String reportPath = System.getProperty("org.episteme.report.path", "docs/HIGH_PRECISION_CORRECTNESS_REPORT.md");
        reporter.exportToRoot(reportPath);
        System.out.println("Compliance report exported to root: " + reportPath);
        
        if (hasFailures) {
            Assertions.fail("Correctness audit failed for one or more operations. See report: " + reportPath);
        }
    }

    @SuppressWarnings("unchecked")
    private <T> void auditProvider(LinearAlgebraProvider<T> prov, LinearAlgebraProvider<RealBig> groundTruth, Map<String, String> metrics) {
        // HighPrecisionAuditOperations uses lambda to report back to our metrics map
        
        // 1. RealBig block
        try {
            HighPrecisionAuditOperations.runRealBigAudit((LinearAlgebraProvider<RealBig>) prov, MATRIX_SIZE, (op, test) -> {
                try {
                    test.get();
                    metrics.put(op, "✅ PASS");
                } catch (UnsupportedOperationException ex) {
                    metrics.put(op, "⚠️ SKIP: " + ex.getMessage());
                } catch (Throwable ex) {
                    String msg = ex.getMessage();
                    if (msg == null || msg.isEmpty()) msg = ex.getClass().getSimpleName();
                    else msg = ex.getClass().getSimpleName() + ": " + (msg.length() > 50 ? msg.substring(0, 47) + "..." : msg);
                    metrics.put(op, "❌ FAIL: " + msg);
                }
            });
        } catch (ClassCastException e) {
             // Provider might not support RealBig
        }

        // 2. Complex block
        try {
            HighPrecisionAuditOperations.runComplexAudit((LinearAlgebraProvider<Complex>) prov, MATRIX_SIZE, (op, test) -> {
                try {
                    test.get();
                    metrics.put(op, "✅ PASS");
                } catch (UnsupportedOperationException ex) {
                    metrics.put(op, "⚠️ SKIP: " + ex.getMessage());
                } catch (Throwable ex) {
                    String msg = ex.getMessage();
                    if (msg == null || msg.isEmpty()) msg = ex.getClass().getSimpleName();
                    else {
                        // Include class name for context
                        msg = ex.getClass().getSimpleName() + ": " + msg;
                    }
                    metrics.put(op, "❌ FAIL: " + msg);
                    // Log the full stack trace for deep debugging
                    System.err.println("Audit failure in " + prov.getName() + " for " + op + ":");
                    ex.printStackTrace();
                }
            });
        } catch (ClassCastException e) {
            // Provider might not support Complex
        }
    }

    private List<LinearAlgebraProvider<?>> discoverHPProviders() {
        List<LinearAlgebraProvider<?>> providers = new ArrayList<>();
        for (LinearAlgebraProvider<?> p : ServiceLoader.load(LinearAlgebraProvider.class)) {
            providers.add(p);
        }
        // Force sort to have deterministic report
        providers.sort(Comparator.comparing(LinearAlgebraProvider::getName));
        return providers;
    }
}
